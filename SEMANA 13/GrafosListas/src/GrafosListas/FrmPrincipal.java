    private void btnRecorrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecorrerActionPerformed
        int v = Integer.parseInt(JOptionPane.showInputDialog ("Recorrer a partir de: "));
        gm.recorridoAnchura(v, modelo1);
        gm.recorridoProfundidad(v, modelo2);
    }//GEN-LAST:event_btnRecorrerActionPerformed

    private void btnInsertarAristaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarAristaActionPerformed
        int i= Integer.parseInt(JOptionPane.showInputDialog ("Ingresar vertice de inicio:"));
        int j= Integer.parseInt(JOptionPane.showInputDialog ("Ingresar vertice de final:"));
        gm.insertarArista(i, j);
        JOptionPane.showMessageDialog(null, "Arista Insertada con éxito");
    }//GEN-LAST:event_btnInsertarAristaActionPerformed

    private void btnEliminarAristaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarAristaActionPerformed
        int i= Integer.parseInt(JOptionPane.showInputDialog ("Ingresar vertice de inicio a eliminar:"));
        int j= Integer.parseInt(JOptionPane.showInputDialog ("Ingresar vertice de final a eliminar:"));
        gm.eliminarArista(i, j);
        JOptionPane.showMessageDialog(null, "Arista Eliminada con éxito");
    }//GEN-LAST:event_btnEliminarAristaActionPerformed

    private void btnEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntradaActionPerformed
        if(gm.esDirigido()){
            int v = Integer.parseInt(JOptionPane.showInputDialog ("Ingresar Vertice :"));
            JOptionPane.showMessageDialog(null, "Grado de Entrada de  "+v+": "+ gm.gradoEntrada(v));
        }else{
            JOptionPane.showMessageDialog(null, "El Grafo NO es Dirigido");
        }
    }//GEN-LAST:event_btnEntradaActionPerformed

    private void btnSalidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalidaActionPerformed
        if(gm.esDirigido()){
            int v = Integer.parseInt(JOptionPane.showInputDialog ("Ingresar Vertice :"));
            JOptionPane.showMessageDialog(null, "Grado de Salida de  "+v+": "+ gm.gradoSalida(v));
        }else{
            JOptionPane.showMessageDialog(null, "El Grafo NO es Dirigido");
        }
    }//GEN-LAST:event_btnSalidaActionPerformed

    private void btnDirigidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDirigidoActionPerformed
        boolean correcto = gm.esDirigido();
        if(correcto)
        JOptionPane.showMessageDialog(null, "ES DIRIGIDO");
        else
        JOptionPane.showMessageDialog(null, "NO ES DIRIGIDO");
    }//GEN-LAST:event_btnDirigidoActionPerformed

    private void btnAdyacentesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdyacentesActionPerformed
        int v= Integer.parseInt(JOptionPane.showInputDialog ("Ingresar Vertice para ver sus adyacentes :"));
        JOptionPane.showMessageDialog(null, "Adyacentes: " + gm.mostrarAdyacentes(v));
    }//GEN-LAST:event_btnAdyacentesActionPerformed

