    public void encolar(Cliente valor) {
        Nodo<Cliente> nuevo = new Nodo(valor);

        if (primero == null) {
            primero = nuevo;
            ultimo = nuevo;
        } else {
            ultimo.setSgte(nuevo);
            ultimo = nuevo;
        }
    }
    
    public void mostrar(DefaultTableModel modelo) {
        modelo.setRowCount(0);
        Object[] titulos = {"Nombre","Monto"};
        
        modelo.setColumnIdentifiers(titulos);
        Nodo<Cliente> actual = primero;

        while (actual != null) {
            Object[] datos = {actual.getInfo().getNombre(),
                actual.getInfo().getMonto()};
            modelo.addRow(datos);
            
            actual = actual.getSgte();
        }  
    }
