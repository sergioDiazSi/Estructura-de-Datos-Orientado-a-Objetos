    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        double valor = Double.parseDouble(txtValor.getText());
        txtValor.setText("");
        txtValor.requestFocus();
        vector.agregar(valor);
        vector.mostrar(modelo);
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        double n = Double.parseDouble(txtValor.getText());
           if(vector.getTotalElementos()>0)
               if(vector.buscar(n)!=-1)
                   JOptionPane.showMessageDialog(this,"Posicion: " + vector.buscar(n),"Posicion",1);
               else
                   JOptionPane.showMessageDialog(this,"Numero no ingresado: \n" + vector.buscar(n),"Error",1);
           else
               JOptionPane.showMessageDialog(this,"Vector sin elementos","Error",0);
    }//GEN-LAST:event_btnBuscarActionPerformed

