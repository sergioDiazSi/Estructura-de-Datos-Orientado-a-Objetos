    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        int valor = Integer.parseInt(txtValor.getText());
        ab.insertar(valor);
        txtValor.setText("");
        txtValor.requestFocus();
        ab.preOrden(modelo1);
        ab.enOrden(modelo2);
        ab.postOrden(modelo3);
        
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int x = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese valor a buscar: "));
        Nodo<Integer> r = ab.buscar(x);
        
        if(r!=null)
            JOptionPane.showMessageDialog(null, "El valor se encuentra en el √°rbol");
        else
            JOptionPane.showMessageDialog(null, "El valor NO se encuentra en el √°rbol");
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int x = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese valor a buscar: "));
        Nodo<Integer> r = ab.buscar(x);
        
        if(r!=null){
            ab.eliminar(x);
            ab.preOrden(modelo1);
            ab.enOrden(modelo2);
            ab.postOrden(modelo3);
            JOptionPane.showMessageDialog(null, "Dato Eliminado");
        }
        else
            JOptionPane.showMessageDialog(null, "El valor NO se encuentra en el √°rbol");
    }//GEN-LAST:event_btnEliminarActionPerformed

